package projeto_final_eng;

public class CarregarArgs{
	
private int args1;
private int args2;

public CarregarArgs(int args1, int args2) {
	super();
	this.args1 = args1;
	this.args2 = args2;
}

public int getArgs1() {
	return args1;
}
public void setArgs1(int args1) {
	this.args1 = args1;
}
public int getArgs2() {
	return args2;
}
public void setArgs2(int args2) {
	this.args2 = args2;
}
}